    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        boolean find = false;
        String pseudo = jTextField1.getText();
        User u;
        for(int i=0;i<connections.getConnectedUsers().getSize();i++){
        	u=connections.getConnectedUsers().getElementAt(i);
            if(u.getPseudo().equals(pseudo))
                find=true;
        }
        if(pseudo.length()==0) {
            jLabel2.setText("Enter a string");
        }
        else if(!find)
        {
            connections.chooseAndSendPseudo(pseudo);
            jPanel1.setVisible(false);
            jPanel3.setVisible(true);
            jTextField1.setText(null);
            jLabel2.setText(null);
           // testMessage("Just for test ");
        }
        else
        {
            jLabel2.setText("Pseudo already used");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

