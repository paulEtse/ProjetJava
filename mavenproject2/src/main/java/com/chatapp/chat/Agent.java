    void filterHistorique(){
       System.out.println(chat.getHistorique());
       chat.getCurrentMessages().removeAllElements();
       List<Message> m= new ArrayList<Message>();
       for(Message msg : chat.getHistorique()){
           if(msg.getSender().equals(chat.getCurrentReceiver())||msg.getReceiver().equals(chat.getCurrentReceiver()))
               m.add(msg);
       }
      Collections.sort(m);
      for(Message msg : m)
          chat.getCurrentMessages().addElement(msg);
    }

