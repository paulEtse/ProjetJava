    public void testMessage(String s)
    {
    	Message m = new Message();
        m.setDate(LocalDateTime.now());
    	m.setSender(connections.getOwner());
    	m.setReceiver(connections.getConnectedUsers().elementAt(0));
    	m.setType(MessageType.Text);
    	m.setText(s);
    	chat.sendMessage(m);
    }
